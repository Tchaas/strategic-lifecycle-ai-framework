let mockState: StrategicLifecycleMockState = structuredClone(seededStrategicLifecycleState);

export const strategicLifecycleMockApi = {
  getState: () => mockState,
  reset: () => {
    mockState = structuredClone(seededStrategicLifecycleState);
    return mockState;
  },
  list: <K extends keyof StrategicLifecycleMockState>(key: K): StrategicLifecycleMockState[K] => mockState[key],
  replace: <K extends keyof StrategicLifecycleMockState>(key: K, value: StrategicLifecycleMockState[K]) => {
    mockState = { ...mockState, [key]: value };
    return mockState[key];
  },
  upsertById: <K extends keyof StrategicLifecycleMockState>(
    key: K,
    record: StrategicLifecycleMockState[K] extends Array<infer T> ? T & { id: string } : never,
  ) => {
    const records = mockState[key] as Array<typeof record>;
    const existingIndex = records.findIndex((candidate) => candidate.id === record.id);
    const nextRecords = existingIndex >= 0
      ? records.map((candidate) => (candidate.id === record.id ? record : candidate))
      : [...records, record];
    mockState = { ...mockState, [key]: nextRecords };
    return record;
  },
};
